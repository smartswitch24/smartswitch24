  function detectLang() {
    var saved = localStorage.getItem('ss24_lang');
    if (saved === 'de' || saved === 'ar') return saved;
    return (navigator.language || '').toLowerCase().startsWith('ar') ? 'ar' : 'de';
  }

  function applyLang(lang) {
    var t = T[lang];
    if (!t) return;
    document.documentElement.lang = lang;
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';

    document.querySelectorAll('[data-i18n]').forEach(function (el) {
      var key = el.getAttribute('data-i18n');
      if (t[key] !== undefined) el.innerHTML = t[key];
    });

    document.querySelectorAll('[data-i18n-aria]').forEach(function (el) {
      var key = el.getAttribute('data-i18n-aria');
      if (t[key] !== undefined) el.setAttribute('aria-label', t[key]);
    });

    document.querySelectorAll('[data-lang-block]').forEach(function (el) {
      el.hidden = el.getAttribute('data-lang-block') !== lang;
    });

    document.querySelectorAll('.lang-btn').forEach(function (btn) {
      btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
    });

    localStorage.setItem('ss24_lang', lang);
  }

  document.addEventListener('DOMContentLoaded', function () {
    var lang = detectLang();
    applyLang(lang);
    document.querySelectorAll('.lang-btn').forEach(function (btn) {
      btn.addEventListener('click', function () { applyLang(this.getAttribute('data-lang')); });
    });
  });

  // Apply dir early to prevent layout flash
  (function () {
    var lang = detectLang();
    if (lang === 'ar') {
      document.documentElement.dir = 'rtl';
      document.documentElement.lang = 'ar';
    }
  }());
